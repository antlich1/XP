namespace CoreLib.Hooking
{
    #region KeyState
    /// <summary>
    /// Enumeration representing key state.
    /// </summary>
    public enum KeyState
    {
        /// <summary>
        /// Key up state.
        /// </summary>
        Up = 0,
        /// <summary>
        /// Key down state.
        /// </summary>
        Down = 1,
        /// <summary>
        /// Any key state.
        /// </summary>
        Any = 2,
    } 
    #endregion
}
